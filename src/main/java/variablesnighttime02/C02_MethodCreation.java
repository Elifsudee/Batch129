package variablesnighttime02;

public class C02_MethodCreation {
    public static void main(String[] args) {


        System.out.println(kareAlan(5, 6));

        kareCevre(5,5);

    }




    public static int kareAlan(int a,int b){

        return a*b;




    }

    public static void kareCevre(int x,int y){

        System.out.println(2*(x+y));
    }




}//class

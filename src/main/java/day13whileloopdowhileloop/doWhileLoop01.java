package day13whileloopdowhileloop;

public class doWhileLoop01 {
    public static void main(String[] args) {

        // While-loop

        int i=1;
        while (i<1){
            System.out.println("sen while loopsun");//while body hic calismadi yani Zero Execution

            i++;
        }

        // do-while-loop

        int k=1;

        do{
            System.out.println("sen bir do-while loopsun");
            k++;
        }while (k<1);







    }
}
